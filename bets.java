    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
        // TODO add your handling code here:
        //Resetear campo premio a 0
        txfPremio.setText("0");

        //Validar campo importe
        String importeCad = txfImporte.getText();
        
        int importe;
        importe = 0;
        
        try 
        {
            //Validar que importe es un número
            String importeStr = null;
            
            importe = Integer.parseInt(importeStr);
            if (importe <= 0) 
            {
                throw new NumberFormatException("Campo mayor que cero.");
            }
        } 
        catch (NumberFormatException e) 
        {
            if (e.getMessage().equals("El importe debe ser mayor que cero.")) 
            {
                JOptionPane.showMessageDialog(this, "Campo mayor que cero", "Error en importe", JOptionPane.ERROR_MESSAGE);
            } 
            else 
            {
                JOptionPane.showMessageDialog(this, "Solo se admiten números", "Error en importe", JOptionPane.ERROR_MESSAGE);
            }
            txfImporte.requestFocus();
            txfImporte.selectAll();
            return;
        }

        //Obtener equipo ingresado
        String equipo = txfEquipo.getText();

        //Crear objeto ApuestaDeportiva
        ApuestaDeportiva apuesta = new ApuestaDeportiva(importe, equipo);

        //Verificar si equipo es el ganador
        if (equipo.equalsIgnoreCase("Villarreal")) 
        {
            int premio;
            premio = 10 * importe;
            
            apuesta.setPremio(premio);
            txfPremio.setText(String.valueOf(premio));
            txfEquipo.requestFocus();
            txfEquipo.selectAll();

            //Rellenar array de carac del equipo
            char[] arrLetrasEquipo = equipo.toCharArray();
            informaArray(arrLetrasEquipo, equipo);
        } 
        else 
        {
            //Limpiar campos
            txfImporte.setText("0");
            txfEquipo.setText("");
            txfPremio.setText("0");
            txfImporte.requestFocus();
            txfImporte.selectAll();
        }
    }//GEN-LAST:event_btnVerificarActionPerformed

