    private void informaArray(char[] arrLetrasEquipo, String equipo) 
    {
        int k;
        
        for (k = 0; k < equipo.length(); k = k + 1) 
        {
            arrLetrasEquipo[k] = equipo.charAt(k);
        }
    }
    
